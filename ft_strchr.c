#include "libft.h"

char	*ft_strchr(char *s, int c)
{
	int	i;

	i = 0;
	if (c == '\0')
		return (&s[i]);
	while (s[i] || s[i] == c)
	{
		if (s[i] == c)
			return (&s[i]);
		i++;
	}
	return (NULL);
}
